function replyRequestsMedia(text) {
  const value = String(text || "");
  return MEDIA_REQUEST_PATTERNS.some((pattern) => pattern.test(value));
}

function sanitizeSupportReply(text) {
  if (replyRequestsMedia(text)) {
    return MEDIA_DECLINE_REPLIES[Math.floor(Math.random() * MEDIA_DECLINE_REPLIES.length)];
  }
  return text;
}

function applyMuhammadTypos(text) {
  let result = String(text || "");
  const swaps = [
    [/\bчто\b/gi, "што"],
    [/\bсейчас\b/gi, "щас"],
    [/\bсмотрю\b/gi, "смтарю"],
    [/\bнапиши\b/gi, "напиш"],
    [/\bподожди\b/gi, "падожди"],
    [/\bхорошо\b/gi, "харашо"],
    [/\bпонял\b/gi, "поня"],
  ];

  for (const [pattern, replacement] of swaps) {
    if (Math.random() < 0.55) {
      result = result.replace(pattern, replacement);
    }
  }

  if (Math.random() < 0.25 && !/брат/i.test(result)) {
    result = `брат ${result}`;
  }

  result = result.replace(/,/g, "");
  if (Math.random() < 0.35) {
    result = result.replace(/\s+([а-яё]{4,})\s+/gi, " $1");
  }

  return result.replace(/\s{2,}/g, " ").trim();
}

function humanizeSupportReply(text, agentName = "") {
  let result = sanitizeSupportReply(String(text || "").trim());
  result = result
    .replace(/[Оо]пиш(ите|и|ь)/g, "напиши")
    .replace(/[Нн]апишите/g, "напиши")
    .replace(/[Рр]асскаж(ите|и)/g, "расскажи")
    .replace(/[Уу]точн(ите|и)/g, "уточни")
    .replace(/,?\s*пожалуйста/gi, "")
    .replace(/[Бб]удьте добры,?\s*/g, "")
    .replace(/[Нн]е могли бы вы/gi, "можешь")
    .replace(/[Бб]лагодар(им|ю)\s+(за|вас)[^.!]*[.!]?\s*/gi, "")
    .replace(/\.\s+/g, " ")
    .replace(/\.+$/g, "")
    .replace(/\s{2,}/g, " ")
    .trim();

  result = result.replace(/,/g, () => (Math.random() < 0.45 ? "" : ","));
  result = result.replace(/\s{2,}/g, " ").trim();

  if (Math.random() < 0.4 && result.length > 1) {
    result = result.charAt(0).toLowerCase() + result.slice(1);
  }

  if (agentName === "Мухаммад") {
    result = applyMuhammadTypos(result);
  }

  return result || "ща гляну напиши ещё раз что не так";
}

async function verifyOpenRouterKey(apiKey) {
  const response = await fetch("https://openrouter.ai/api/v1/auth/key", {
    headers: { Authorization: `Bearer ${apiKey}` },
  });
  const data = await response.json().catch(() => ({}));
  if (!response.ok) {
    const detail = data?.error?.message || `HTTP ${response.status}`;
    return { ok: false, error: detail };
  }
  return { ok: true, data: data?.data || data };
}

